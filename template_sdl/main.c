struct _screen {
    unsigned char c;
    struct {
        unsigned char r;
        unsigned char g;
        unsigned char b;
    } fore;
    struct {
        unsigned char r;
        unsigned char g;
        unsigned char b;
    } back;
};

#define txt_width  40
#define txt_height 25

static int cur_x;
static int cur_y;
static int cursor_visible;
static struct _screen screen[txt_width * txt_height];
static SDL_Rect crect;

void SDL_DrawString(const char *s) {
    char c;

    while (*s) {
        c = *s++;
        if (c == '\r') {
            cur_x = 0;
        }
        else if (c == '\n') {
            cur_y++;
            if (cur_y >= txt_height) {
                memcpy(&screen[0], &screen[txt_width], sizeof(screen) - sizeof(struct _screen) * txt_width);
                for (int i = txt_width * (txt_height - 1); i < txt_width * txt_height; i++) {
                    screen[i].c = ' ';
                    screen[i].fore.r = 255;
                    screen[i].fore.g = 255;
                    screen[i].fore.b = 255;
                    screen[i].back.r = 98;
                    screen[i].back.g = 0;
                    screen[i].back.b = 32;
                }
                cur_y--;
            }
        }
        else {
            screen[cur_y * txt_width + cur_x].c = c;
            cur_x++;
            if (cur_x >= txt_width) {
                cur_x = 0;
                cur_y++;
                if (cur_y >= txt_height) {
                    memcpy(&screen[0], &screen[txt_width], sizeof(screen) - sizeof(struct _screen) * txt_width);
                    for (int i = txt_width * (txt_height - 1); i < txt_width * txt_height; i++) {
                        screen[i].c = ' ';
                        screen[i].fore.r = 255;
                        screen[i].fore.g = 255;
                        screen[i].fore.b = 255;
                        screen[i].back.r = 98;
                        screen[i].back.g = 0;
                        screen[i].back.b = 32;
                    }
                    cur_y--;
                }
            }
        }
    }
    cursor_visible = 1;
}

